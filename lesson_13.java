class Cat6 {
    private String name;
    private int age;
    private int weight;
    private int speed;

    public Cat6(String name, int age, int weight, int speed) {

    }
    public void setWeight(int weight) {
        this.weight = weight;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getWeight() {
        return this.weight;
    }

    public int getSpeed() {
        return this.speed;
    }
    public String getName() {
        return this.name;
    }

    public int getAge() {
        return this.age;
    }
}